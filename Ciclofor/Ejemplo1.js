// 1-Crea un array con los días de la semana y utiliza un bucle for para imprimir cada día en la consola.

const arrayDiasdelaSemana = ['Lunes','Martes','Miercoles','Jueves','Viernes','Sabado','Domingo']
for(let i=0; i<arrayDiasdelaSemana.length; i++ ){
    console.log(`Día ${i + 1}: ${arrayDiasdelaSemana[i]}`)
}